// Selected Products Array
let selectedProducts = [];

// Function to Attach Event Listeners to Compare Buttons
function attachCompareEventListeners() {
    document.querySelectorAll('.compare-btn').forEach(button => {
        button.addEventListener('click', () => {
            const productId = button.getAttribute('data-id');
            
            // Check if Product Already Selected
            if (selectedProducts.includes(productId)) {
                alert("Product already selected for comparison.");
                return;
            }

            // Add Product to Comparison (max 3)
            if (selectedProducts.length < 3) {
                selectedProducts.push(productId);
                updateComparisonTable();
            } else {
                alert("You can only compare up to 3 products.");
            }
        });
    });
}

// Update Comparison Table
function updateComparisonTable() {
    // Show Comparison Section
    document.getElementById('comparison-section').style.display = 'block';

    // Update Table Headers and Rows
    selectedProducts.forEach((productId, index) => {
        document.getElementById(`product-${index + 1}`).innerText = products[productId].name;
        document.getElementById(`price-${index + 1}`).innerText = products[productId].price;
        document.getElementById(`material-${index + 1}`).innerText = products[productId].material;
        document.getElementById(`lens-${index + 1}`).innerText = products[productId].lens;
    });

    // Clear Empty Columns
    for (let i = selectedProducts.length + 1; i <= 3; i++) {
        document.getElementById(`product-${i}`).innerText = "";
        document.getElementById(`price-${i}`).innerText = "";
        document.getElementById(`material-${i}`).innerText = "";
        document.getElementById(`lens-${i}`).innerText = "";
    }
}

// Clear Comparison
document.getElementById('clear-comparison').addEventListener('click', () => {
    selectedProducts = [];
    document.getElementById('comparison-section').style.display = 'none';
});

